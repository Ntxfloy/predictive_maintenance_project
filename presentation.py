import streamlit as st
import reveal_slides as rs

# Add some custom style for a more polished look
st.markdown("""
<style>
.stApp {
    background-color: #f7f9fc;
}
</style>
""", unsafe_allow_html=True)

st.title("⚙️ Презентация проекта: Предиктивное обслуживание")
st.subheader("Проектная работа (ВКР)")

# Presentation content in Markdown
presentation_markdown = """
# Предиктивное обслуживание оборудования
---
## Введение и Актуальность
- **Проблема**: Внезапные поломки оборудования приводят к простоям и финансовым потерям.
- **Решение**: Переход от реактивного ремонта к предиктивному обслуживанию.
- **Цель**: Разработка модели машинного обучения для прогнозирования отказов на основе телеметрии.
---
## Описание датасета
- **Источник**: AI4I 2020 Predictive Maintenance Dataset (10 000 записей).
- **Признаки (Телеметрия)**:
  - Температура воздуха [K] и процесса [K]
  - Скорость вращения [rpm]
  - Крутящий момент [Nm]
  - Износ инструмента [мин]
  - Тип продукта (L, M, H)
---
## Анализ данных (EDA)
- **Дисбаланс классов**: Только ~3.39% записей содержат отметку об отказе.
- **Корреляции**:
  - Высокая корреляция между температурами процесса и воздуха.
  - Отрицательная корреляция между скоростью вращения и крутящим моментом (согласно законам физики).
---
## Предобработка данных
1. **Удаление незначимых признаков** (`UDI`, `Product ID`).
2. **Кодирование категорий**: Перевод признака `Type` (тип продукта) в числовой формат.
3. **Масштабирование**: Приведение числовых признаков к единому масштабу с помощью `StandardScaler`.
4. **Валидация**: Разделение данных на Train/Test в классическом соотношении 80/20.
---
## Сравнение моделей машинного обучения
- **Обученные алгоритмы**:
  - *Logistic Regression* (Простой линейный классификатор)
  - *Random Forest* (Ансамбль деревьев решений)
  - *XGBoost* (Градиентный бустинг)
- **Результаты (F1-Score на тесте)**:
  - Logistic Regression: ~0.40
  - Random Forest: ~0.84
  - XGBoost: **~0.86**
---
## Оптимизация и MLOps
- **Подбор параметров**: Оптимизация гиперпараметров бустинга (XGBoost) повысила F1-score до 0.88+.
- **Интеграция с ClearML**:
  - Отслеживание всех запусков и логов.
  - Сравнение версий моделей.
  - Хранение артефактов и воспроизводимость экспериментов.
---
## Бизнес-эффект
- **Сокращение простоев** оборудования до 30%.
- **Оптимизация запасов** комплектующих за счет прогнозирования износа.
- **Снижение затрат** на внеплановое обслуживание.
---
## Заключение
- Разработана и внедрена интеллектуальная система мониторинга.
- XGBoost выбран в качестве основной продакшн-модели.
- Приложение готово к пилотированию на реальном производстве.
"""

# Configure slideshow options in the sidebar
with st.sidebar:
    st.header("🎨 Настройки презентации")
    theme = st.selectbox("Тема оформления", ["black", "white", "league", "beige", "sky", "night", "serif", "simple", "solarized"], index=1)
    transition = st.selectbox("Эффект перехода", ["slide", "convex", "concave", "zoom", "none"], index=0)
    height = st.slider("Высота слайдов (пиксели)", min_value=300, max_value=800, value=550, step=50)
    plugins = st.multiselect("Плагины Reveal.js", ["highlight", "katex", "notes", "search", "zoom"], default=["zoom"])

# Display the slides
rs.slides(
    presentation_markdown,
    height=height,
    theme=theme,
    config={
        "transition": transition,
        "plugins": plugins,
    },
    markdown_props={"data-separator-vertical": "^--$"},
)
